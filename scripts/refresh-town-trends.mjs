// Pulls last 24 months of HDB resale transactions from data.gov.sg,
// computes the monthly median price per (town, flat-type), and writes
// data/town-trends.json — used by the price-trend charts on /towns/ pages.
//
// Run: `node scripts/refresh-town-trends.mjs`
// Auto-runs monthly via .github/workflows/refresh-valuation.yml
//
// data.gov.sg public dataset (resource_id is stable):
//   d_8b84c4ee58e3cfc0ece0d773c8ca6abc — HDB Resale Flat Prices (Jan-2017 onwards)

import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const OUT_PATH = path.resolve(__dirname, '..', 'data', 'town-trends.json');

const RESOURCE_ID = 'd_8b84c4ee58e3cfc0ece0d773c8ca6abc';
const API = `https://data.gov.sg/api/action/datastore_search`;
const PAGE = 5000;
const WINDOW_MONTHS = 24;

const TOWNS = [
  'Punggol', 'Sengkang', 'Tampines', 'Toa Payoh', 'Bishan', 'Bedok',
  'Pasir Ris', 'Hougang', 'Ang Mo Kio', 'Yishun', 'Woodlands', 'Sembawang',
  'Bukit Batok', 'Bukit Panjang', 'Choa Chu Kang', 'Jurong East', 'Jurong West',
  'Clementi', 'Queenstown', 'Bukit Merah', 'Kallang/Whampoa', 'Geylang',
  'Marine Parade', 'Serangoon',
];

const FLAT_TYPES = {
  '3-Room': '3 ROOM',
  '4-Room': '4 ROOM',
  '5-Room': '5 ROOM',
};

function monthFloor(d) {
  return d.toISOString().slice(0, 7); // YYYY-MM
}

function windowStart() {
  const d = new Date();
  d.setDate(1);
  d.setMonth(d.getMonth() - WINDOW_MONTHS);
  return monthFloor(d);
}

function monthRange(start, end) {
  const out = [];
  let [y, m] = start.split('-').map(Number);
  while (true) {
    const s = `${y}-${String(m).padStart(2, '0')}`;
    if (s > end) break;
    out.push(s);
    m += 1;
    if (m > 12) { m = 1; y += 1; }
  }
  return out;
}

function median(arr) {
  const s = arr.slice().sort((a, b) => a - b);
  const mid = Math.floor(s.length / 2);
  return s.length % 2 ? s[mid] : (s[mid - 1] + s[mid]) / 2;
}

async function fetchAll() {
  const start = windowStart();
  const all = [];
  let offset = 0;

  console.log(`Fetching HDB resale transactions from ${start} onwards...`);

  while (true) {
    const url = `${API}?resource_id=${RESOURCE_ID}&limit=${PAGE}&offset=${offset}&sort=month%20desc`;
    const res = await fetch(url);
    if (!res.ok) throw new Error(`HTTP ${res.status}: ${await res.text()}`);
    const json = await res.json();
    const records = json?.result?.records ?? [];
    if (!records.length) break;

    let stopped = false;
    for (const r of records) {
      if (r.month < start) { stopped = true; continue; }
      all.push(r);
    }

    process.stdout.write(`\r  fetched ${all.length} records (offset ${offset + records.length})`);

    if (stopped || records.length < PAGE) break;
    offset += PAGE;
  }

  console.log(`\nDone. ${all.length} records in window.`);
  return all;
}

async function main() {
  const records = await fetchAll();
  const start = windowStart();
  const end = monthFloor(new Date());
  const months = monthRange(start, end);

  // bucket prices ($K) per town → flat type → month
  const buckets = {};
  for (const r of records) {
    const town = TOWNS.find(t => t.toUpperCase() === r.town?.trim().toUpperCase());
    const flat = Object.entries(FLAT_TYPES).find(([_, v]) => v === r.flat_type?.trim().toUpperCase())?.[0];
    const price = Number(r.resale_price);
    if (!town || !flat || !Number.isFinite(price)) continue;
    ((buckets[town] ??= {})[flat] ??= {})[r.month] ??= [];
    buckets[town][flat][r.month].push(price / 1000);
  }

  const byTown = {};
  for (const town of Object.keys(buckets).sort()) {
    byTown[town] = {};
    for (const flat of Object.keys(FLAT_TYPES)) {
      const mm = buckets[town][flat] ?? {};
      const p50 = months.map(m => mm[m]?.length ? Math.round(median(mm[m])) : null);
      const count = months.map(m => mm[m]?.length ?? 0);
      if (p50.some(v => v !== null)) byTown[town][flat] = { p50, count };
    }
  }

  const output = {
    _meta: {
      description: 'Monthly median HDB resale prices per town and flat type, in $K. Arrays align with `months`.',
      source: `data.gov.sg — ${RESOURCE_ID} (HDB Resale Flat Prices)`,
      lastUpdated: new Date().toISOString().slice(0, 10),
      lastUpdatedNote: `Generated by scripts/refresh-town-trends.mjs from last ${WINDOW_MONTHS} months of transactions.`,
      windowMonths: WINDOW_MONTHS,
      totalRecords: records.length,
    },
    months,
    byTown,
  };

  await fs.writeFile(OUT_PATH, JSON.stringify(output) + '\n', 'utf8');
  console.log(`Wrote ${OUT_PATH}`);
  console.log(`Towns with data: ${Object.keys(byTown).length}`);
}

main().catch((err) => {
  console.error('refresh-town-trends failed:', err);
  process.exit(1);
});
